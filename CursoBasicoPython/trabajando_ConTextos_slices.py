#slice/rebanada

nombre=input("New university")

rebanada1=nombre[-2:]
print(rebanada1)
    #zi
rebanada2=nombre[1:4]
print(rebanada2)
    #lat
rebanada3=nombre[1:5:2]
print(rebanada3)
    #lt

rebanada4=nombre[::]  #Desde el inicio hasta el final de 1 en 1
print(rebanada4)
    #Platzi

rebanada5=nombre[::2]
print(rebanada5)
    #Paz

rebanada_reversa=nombre[::-1] #De uno en uno desde el final al principio
print(rebanada_reversa)
    #iztalP

